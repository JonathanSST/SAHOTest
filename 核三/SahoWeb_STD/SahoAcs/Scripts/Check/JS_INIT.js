/*
=========================================================================================
// 網頁載入執行
=========================================================================================*/
var oINIT_PARAM = {
    //啟用參考列功能
    REF_INIT: true
};

$(document).ready(function () {
    var oFORM = JsFunGET_PARAM("FORM");
    oFORM[0] = $("form")[0];

    //主頁面初始化
    if (typeof (JsFunEVENT_COMMAND) == 'function')
        JsFunEVENT_COMMAND("MASTER_PAGE_INIT", { sender: this, EventArgs: null });

    /*
    =========================================================================================
    設定[按鈕列]樣式
    =========================================================================================*/
    $('DIV[id$="CONTENT_BUTTON"]').find(':button').button();

    /*
    =========================================================================================
    建立提示文字
    =========================================================================================*/
    JsFunTOOLTIP_CREATE();

    /*
    =========================================================================================
    建立 TAB_BAR
    =========================================================================================*/
    JsFunTAB_BAR_CREATE(oFORM[0]);

    /*
    =========================================================================================
    建立 TR_EVENT
    =========================================================================================*/
    //JsFunTR_EVENT_CREATE();

    /*
    =========================================================================================
    建立抬頭列
    =========================================================================================*/
    //JsFunTABLE_PAGE_BAR_CREATE('table[STYLE_M="02"]');
    JsFunTABLE_CREATE_PAGE_BAR('table[STYLE_M="02"]');

    /*
    =========================================================================================
    // 快取 TABLE 一列, 當新增一列時使用
    =========================================================================================*/
    JsFunTABLE_ROW_CACHE('table[STYLE_M="02"]');

    /*
    =========================================================================================
    //建立階層按鈕
    =========================================================================================*/
    //JsFunLEVEL_BUTTON_CREATE(oFORM[0][0]);

    /*
    =========================================================================================
    //將階層資料列新增
    =========================================================================================*/
    //JsFunLEVEL_ROW_ADD(oFORM[0][0]);

    /*
    =========================================================================================
    //建立參考欄位
    =========================================================================================*/
    if (typeof (JsFunEVENT_COMMAND) == 'function')
        JsFunEVENT_COMMAND("REF_INIT", { sender: this, EventArgs: oINIT_PARAM });
    if (oINIT_PARAM["REF_INIT"] == true) {
        JsFunREF_INIT(oFORM[0]);
    }

    //主頁面已載入
    if (typeof (JsFunEVENT_COMMAND) == 'function')
        JsFunEVENT_COMMAND("MASTER_PAGE_LOAD", { sender: this, EventArgs: null });
});